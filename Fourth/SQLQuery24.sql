SELECT NAME, STARNAME
FROM STUDIO,STARSIN,MOVIE
WHERE STUDIO.NAME = MOVIE.STUDIONAME AND MOVIE.TITLE = STARSIN.MOVIETITLE
ORDER BY STUDIO.NAME

----------------
SELECT DISTINCT STARNAME
FROM STARSIN, MOVIE, MOVIEEXEC
WHERE STARSIN.MOVIETITLE = MOVIE.TITLE AND MOVIE.PRODUCERC# = MOVIEEXEC.CERT# AND MOVIEEXEC.NETWORTH = (
SELECT TOP 1 NETWORTH
FROM MOVIEEXEC
ORDER BY NETWORTH DESC
)

