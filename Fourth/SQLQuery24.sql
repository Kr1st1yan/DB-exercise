SELECT DISTINCT MOVIEEXEC.NAME
FROM MOVIEEXEC, MOVIE, STARSIN
WHERE MOVIEEXEC.CERT# = MOVIE.PRODUCERC# AND MOVIE.TITLE = STARSIN.MOVIETITLE AND STARSIN.STARNAME = 'Harrison Ford'

