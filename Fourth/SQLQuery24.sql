SELECT NAME, TITLE
FROM MOVIE, MOVIEEXEC
WHERE MOVIE.PRODUCERC# = MOVIEEXEC.CERT# AND MOVIEEXEC.NAME = (
SELECT NAME FROM 
MOVIEEXEC, MOVIE
WHERE MOVIE.PRODUCERC# = MOVIEEXEC.CERT# AND MOVIE.TITLE = 'Star Wars'
)

